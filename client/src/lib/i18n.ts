/**
 * Translate a key to the user's language
 * Falls back to English if translation not available
 * 
 * @param key - Translation key (e.g., 'tab.overview')
 * @param locale - Optional locale override
 * @returns Translated string
 */
export function t(key: string, locale?: string): string {
  const language = getLanguageCode(locale);
  const languageTranslations = translations[language] || translations.en;
  
  // Return translation if available, otherwise return English fallback
  return languageTranslations[key] || translations.en[key] || key;
}

/**
 * Get all translations for a specific language
 * Useful for bulk translation or language switching
 */
export function getTranslations(language?: string): Record<string, string> {
  const lang = language ? getLanguageCode(language) : getLanguageCode();
  return translations[lang] || translations.en;
}

/**
 * Check if a translation exists for a key
 */
export function hasTranslation(key: string, locale?: string): boolean {
  const language = getLanguageCode(locale);
  const languageTranslations = translations[language] || translations.en;
  return key in languageTranslations;
}

/**
 * Add or update a translation
 * Useful for dynamic translations or user customization
 */
export function setTranslation(key: string, value: string, language?: string): void {
  const lang = getLanguageCode(language);
  if (!translations[lang]) {
    translations[lang] = {};
  }
  translations[lang][key] = value;
}

